function getColor(i) {
    switch (i) {
        case 0: return color(0,0,0);
        case 1: return color(255, 51, 153);
        case 2: return color(255, 153, 0);
        case 3: return color(255, 255, 0);
        case 4: return color(255, 0, 0);
        case 5: return color(0, 204, 0);
        case 6: return color(0, 204, 255);
        case 7: return color(89, 0, 179);
        case 8: return color(255, 255, 255);
    }
}   